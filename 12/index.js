const diaDaSemana = 2;
let feira = diaDaSemana % 7

if (feira === 0) { console.log("domingo") }
else if (feira === 1) { console.log("segunda-feira") }
else if (feira === 2) { console.log("terça-feira") }
else if (feira === 3) { console.log("quarta-feira") }
else if (feira === 4) { console.log("quinta-feira") }
else if (feira === 5) { console.log("sexta") }
else if (feira === 6) { console.log("sábado") }

